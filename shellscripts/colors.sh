#!/usr/bin/env bash
cyan='\e[0;36m'
green='\e[0;34m'
oke_green='\033[92m'
white='\e[1;37m'
light_green='\e[1;32m'
red='\e[1;31m'
blueF='\e[1;34m'
orange='\e[38;5;166m'

echo -e "$cyan" "I am cyan"
echo -e "$green" "I am green"
echo -e "$oke_green" "I am oke_green"
echo -e "$white" "I am white"
echo -e "$light_green" "I am light_green"
echo -e "$red" "I am red"
echo -e "$blueF" "I am blueF"
echo -e "$orange" "I am orange"
